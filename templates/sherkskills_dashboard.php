				<h3>Thank you and Enjoy!</h3>
				Welcome to Sherk Skills WordPress Plugin.<br/>
				It is now easy to present our set of skills to be added to our Personal WordPress Websites. The plugin creates post type for skills having featured image into it.<br/>
The best thing about the plugin is that you are able to replace all the skills tag that found at the content sitewide with the link to your skills landing page. <br/>
You can also add your skills to your widgets area through widget functionality. A carousel of featured images from your skills are added to your widget contents.<br/>
Added shortcode functionality to display a carousel of skills <b><br/>[sherkskills title="These are my Skills"]</b>.<br/> 

Create landing pages for your skills with description, trainings, related videos and related links. Search the contents of the website with your <b>skills tags</b> and replaced links to your skills landing page. Widgets with beautiful carousel using featured images set to your landing pages. 
			</p>
