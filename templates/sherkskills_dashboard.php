			<h3>Installation</h3>
			<ol>
				<li>BACKUP everything before you install the plugin.</li>
				<li>Upload sherk-skills directory to the `/wp-content/plugins/` directory</li>
				<li>Activate the plugin through the 'Plugins' menu in WordPress</li>	
				<li>Update the Permalinks on settings.</li>
				<li>SherkSkills post type should now available at dashboard and start creating sherkskills post type.</li>
			</ol>
			
			<h3>Basic Instruction on Creating a SherkBanner Post types</h3>

			<ol>
				<li>Create Sherk Skill Post Type</li>
				<li>Add Sherk Skill tag at the upper right on the edit Sherk Skill view.</li>
				<li>Add Sherk Skill category.</li>
				<li>Add Featured Image for the Sherk Skill which is to be shown at the frontend and Widgets.</li>
				<li>Add Website References for your Skills.</li>
				<li>Add Video References for your Skills.</li>
				<li>Add the Sherk Skill widgets to your widget area.</li>
				<li>Added shortcode functionality to display a carousel of skills. <br/>ex. <b>[sherkskills title="These are my Skills"]</b></li>
			</ol>
			
			<h3>Display Slideshow using Dashboard Widget</h3>
            <ol>			
				<li>Go to Dashboard-> Appearance -> Widgets (/wp-admin/widgets.php)</li>
				<li>Look for Sherk Skills Slider Widget and drag it to the widget region you would like it to be shown.</li>
				<li>Configure your Sherk Skills Slider Widget, add title and save.</li>
			</ol>

			<h3>Display Slideshow using Shortcode</h3>
			<ol>
				<li>Shortcode template is shown at the edit/add SherkSkills post type which looks like this:<br/>
				<b>[sherkskills title="These are my Skills"]</b></li>
				<li>Copy the shortcode, paste it to the content text editor and update the title of your shortcode.</li>
			</ol>
			<h3>Parameters Available</h3>

				<h4><b>title</b></h4>
				Title of your Skills Slider<br/>
				default: ''<br/>
				options: string<br/><br/>
		
